def get_cycle_phase(day: int) -> tuple[str, str]:
    """Возвращает (название фазы, рекомендация)"""
    if 1 <= day <= 7:
        return (
            "Менструация 🔴",
            "Снизь веса на 20–30%. Можно пропустить тренировку — это нормально."
        )
    elif 8 <= day <= 13:
        return (
            "Фолликулярная 🟢",
            "Сейчас максимальная энергия! Пробуй новые веса, добавляй нагрузку."
        )
    elif 14 <= day <= 16:
        return (
            "Овуляция 🟡",
            "Пик силы! Следи за техникой — в это время выше риск травм."
        )
    else:
        return (
            "Лютеиновая 🟠",
            "Постепенно снижай нагрузку к концу фазы. Больше акцент на технику."
        )


def get_today_workout(day_of_week: int) -> str:
    """Предлагает тренировку по дню недели (0=пн)"""
    schedule = {0: "A", 2: "B", 4: "C"}  # Пн, Ср, Пт
    return schedule.get(day_of_week)
