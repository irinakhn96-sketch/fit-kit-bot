"""
Расписание тренировок — 4 блока по 2 недели (8 недель итого)
Блок определяется автоматически по дате старта программы.
"""

from datetime import date, timedelta

# Дата старта программы — меняй когда начинаешь новый цикл
PROGRAM_START_DATE = date(2026, 6, 5)


def get_current_block() -> int:
    """Возвращает номер текущего блока (1-4) исходя из даты старта"""
    days_passed = (date.today() - PROGRAM_START_DATE).days
    if days_passed < 0:
        return 1
    block = (days_passed // 14) % 4  # каждые 14 дней, цикл из 4 блоков
    return block + 1


def get_block_info() -> dict:
    """Возвращает инфо о текущем блоке и когда следующий"""
    days_passed = max(0, (date.today() - PROGRAM_START_DATE).days)
    block_num = get_current_block()
    days_in_block = days_passed % 14
    days_left = 14 - days_in_block
    next_change = date.today() + timedelta(days=days_left)
    block = ALL_BLOCKS[block_num]
    return {
        "block": block_num,
        "name": block["name"],
        "weeks": block["weeks"],
        "focus": block["focus"],
        "days_left": days_left,
        "next_change": next_change.strftime("%d.%m.%Y"),
    }


