def find_food(query: str):
    """Ищет продукт по названию (частичное совпадение)"""
    query = query.lower().strip()
    # Точное совпадение
    if query in FOODS:
        return query, FOODS[query]
    # Частичное
    for name, values in FOODS.items():
        if query in name or name in query:
            return name, values
    return None, None


def calc_kbzhu(food_name: str, grams: float):
    """Считает КБЖУ для заданного количества граммов"""
    name, per100 = find_food(food_name)
    if not per100:
        return None
    factor = grams / 100
    return {
        "name": name,
        "grams": grams,
        "calories": round(per100[0] * factor, 1),
        "protein":  round(per100[1] * factor, 1),
        "fat":      round(per100[2] * factor, 1),
        "carbs":    round(per100[3] * factor, 1),
    }


